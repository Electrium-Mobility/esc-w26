/*******************************************************************************************************************************
 * Private Variables
 *******************************************************************************************************************************/
static const uint8_t hall_to_step[8] = {
    0xFFU, /* 000 */
    2U,    /* 001 */
    4U,    /* 010 */
    3U,    /* 011 */
    0U,    /* 100 */
    1U,    /* 101 */
    5U,    /* 110 */
    0xFFU  /* 111 */
};

/*******************************************************************************************************************************
 * Function Definitions
 *******************************************************************************************************************************/

bool trapezoidal_init(const MotorConfig_t *cfg)
{
    (void)cfg;
    return cfg != NULL;
}

uint8_t trapezoidal_hall_to_step(uint8_t hall)
{
    return hall < 8U ? hall_to_step[hall] : 0xFFU;
}
